const MERGE_MENU_LABELS = new Set(['Featured', 'Drinks']);

function buildUnifiedMenu() {
  const categories = [];
  const usedIds = new Set();

  const uniqueId = baseId => {
    let id = baseId;
    let count = 2;
    while (usedIds.has(id)) {
      id = `${baseId}-${count}`;
      count += 1;
    }
    usedIds.add(id);
    return id;
  };

  Object.values(window.MENUS_BY_OUTLET).forEach(outletMenu => {
    outletMenu.forEach(category => {
      const mergeTarget = MERGE_MENU_LABELS.has(category.label)
        ? categories.find(entry => entry.label === category.label)
        : null;

      if (mergeTarget) {
        category.items.forEach(item => {
          if (!mergeTarget.items.some(existing => existing.name === item.name)) {
            mergeTarget.items.push(item);
          }
        });
        return;
      }

      categories.push({
        id: uniqueId(category.id),
        label: category.label,
        items: category.items.map(item => ({ ...item })),
      });
    });
  });

  const drinks = categories.find(entry => entry.label === 'Drinks');
  const teaDrinks = categories.find(entry => entry.label === 'Tea & Drinks');
  if (drinks && teaDrinks) {
    teaDrinks.items.forEach(item => {
      if (!drinks.items.some(existing => existing.name === item.name)) {
        drinks.items.push(item);
      }
    });
    return categories.filter(entry => entry.label !== 'Tea & Drinks');
  }

  if (teaDrinks) {
    teaDrinks.label = 'Drinks';
    teaDrinks.id = uniqueId('drinks');
  }

  return categories;
}

window.MENU = buildUnifiedMenu();
